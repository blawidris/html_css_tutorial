# What is HTML (HyperText Markup Language)
- Used to structure content on a web page (image, text, form, etc)
- Web content are structured using ***HTML tags***
- Some ***HTML tags*** has a open/end tag while some only has a open tag


**Syntax**

```
<!DOCTYPE html>
<html lang="en">
<head>
    <title>HTML Boilerplate</title>
</head>
<body>
    HTML body
</body>
</html>
```